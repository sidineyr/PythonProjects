import matplotlib.pyplot as plt

# definindo os pontos iniciais e finais da linha
x = [2, 4]
y = [2, 4]

# criando o gráfico
plt.plot(x, y)

# adicionando os rótulos do eixo x e y
plt.xlabel('Eixo x')
plt.ylabel('Eixo y')

# adicionando o título do gráfico
plt.title('Gráfico Cartesiano')

# exibindo o gráfico
plt.show()
